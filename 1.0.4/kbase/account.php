<h1>Rule 6 - Account Sharing/Trading</h1>
<body>
<span style="text-align: right; display: block;">Effective date: 16th January 2007</span><br>
<br>
Each account should only be used by ONE person. Account sharing is NOT allowed.<br>
<br>
You may not sell, transfer or lend your account to anyone else, or permit anyone else to use your account, and you may not accept an account that anybody else offers you.<br>
<br>
<div style="text-align: center;"><b>Related Questions</b><br>
Below are some related questions which may help you to further understand and follow this rule.</div><br>
<br>
<h4 style="display: inline;">
Why do we have this rule?
</h4><br>
<br>
We have this rule for two reasons:<br>
<ul style="margin-top: 0px; margin-bottom: 0px;">
<li>Firstly, many of the accounts people try to sell are actually accounts they have stolen, so, by buying the account you are helping them profit from their crime, and encouraging them to do it more!<br>
</li>
<li>Secondly, this rule is for your protection, as people who share accounts run the risk that the person using the account won't give it back, or that the person will break a game rule causing your account to be banned.
</li>
</ul><br>
<br>
<h4 style="display: inline;">
What about swapping my account with another player?
</h4><br>
<br>
This is considered account trading, so it is not allowed under the game rules.<br>
<br>
<h4 style="display: inline;">
Can I sell the 'time' I have put into the account?
</h4><br>
<br>
No. Under the game rules each account should only be used by one person. This applies throughout the lifetime of that account. In order to sell the 'time' you have put into an account, you would have to transfer or lend the account to someone else, thereby breaking the game rules.<br>
<br>
<h4 style="display: inline;">
My 'brother/sister/friend/other' borrowed my account and broke a rule and now it's banned: Please can you unban it?
</h4><br>
<br>
Account sharing is not allowed, so if you break this rule and do so anyway you will be responsible for the consequences. The offence won't be removed because you shouldn't have let someone else use your account in the first place.<br>
<br>
<h4 style="display: inline;">
I share my account with a family member and/or friend, is that allowed?
</h4><br>
<br>
No, this is not allowed. Sharing your account with anyone, including family members and/or friends, means that you are breaking a game rule and can lead to your account being banned.<br>
<br>
<h4 style="display: inline;">
I have seen an account for sale on Ebay, what should I do?
</h4><br>
<br>
Please rest assured that members of Jagex staff check all known auction sites on a daily basis to ensure that no <?php echo $data['wb_name']; ?> accounts are sold via online auctioning web sites.<br>
<br>
It should be remembered that all accounts (and items) remain the property of Jagex at all times, and we will not hesitate to remove such items from the game if we have evidence that rule breaking has taken place.<br>
<br>
<i>To keep <?php echo $data['wb_name']; ?> enjoyable for everyone please observe and follow this rule. We will not tolerate disruptive players, and if you break our rules your account is likely to be banned. Members who are banned for breaking our rules are not entitled to any sort of refund.</i>
